function rankId(cardId) {
  return cardId.split('_')[1];
}

export function minorEmotion(card, rev, slot) {
  const r = RANK[rankId(card.id)];
  const s = SUIT[card.suit];
  const k = rev ? card.keywordReversed : card.keywordUpright;
  const n = card.name;

  if (slot === 'past') {
    return rev
      ? `这张牌说明，你过去在感情里，${s.realm}这条路走得有点拧。
过去可能经历过：
· ${r.past.join('\n· ')}
· 后来你发现：${k}
你不是不会爱，是那时还没学会保护自己。`
      : `这张牌说明，你过去的感情底色，跟「${s.realm}」有关。
过去可能经历过：
· ${r.past.join('\n· ')}
${s.single}
这张牌也在说：${k}，其实早就埋下过伏笔。`;
  }

  if (slot === 'present') {
    return rev
      ? `你现在的感情状态，核心关键词是：
卡住
心口不一
怕受伤先刺人
${r.now.join('\n')}
你最近更像是在：
「我其实在意，但我先装作无所谓。」
如果目前有暧昧对象，这张牌表示：
${n}逆位——${k}
对方会累，你也累，需要一句真话。`
      : `你现在的感情状态，核心关键词是：
${r.now.join('\n')}
你最近更像是在：
「我在看，也在等一个对的感觉。」
如果目前有暧昧对象，这张牌表示：
${k}
${s.single}`;
  }

  if (!rev && card.suit === 'cups' && rankId(card.id) === '02') {
    return `这是一张很好的感情牌。
它代表：
双向奔赴
情绪回应
关系确认
真正聊得来的人出现
未来一段时间，你的感情运势不是「轰轰烈烈型」，而是：
有人开始真正理解你。
重点是「舒服」和「自然」。
如果你单身：
很可能通过日常接触、朋友关系、工作学习圈慢慢发展
不是特别戏剧化的相遇
但会越来越稳定
如果你已有对象：
关系会进入更平衡的阶段
之前没说开的事，有机会慢慢谈明白`;
  }

  return rev
    ? `这张牌提示：感情路上，你还卡在一些旧习惯里。
${n}逆位在说：${k}
如果你单身：
别在不清不楚里耗
如果你已有对象：
把最刺的那句话，换成最真的一句`
    : `这是一张不错的感情牌。
它代表：
${r.fut.join('\n')}
接下来一段时间，主线是：${k}
如果你单身：
${s.single}
如果你已有对象：
${s.couple}`;
}
