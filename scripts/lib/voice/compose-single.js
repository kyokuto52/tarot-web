/** 单张：今日运势 / 问题解答 / 人生建议 */

function kw(card, rev) {
  return rev ? card.keywordReversed : card.keywordUpright;
}

export function composeToday(card, rev) {
  const k = kw(card, rev);
  const n = card.name;
  if (!rev) {
    return `就今天来说，${n}正位的主调是：
${k}

你今天更适合：
· 做一件你一直拖着的小事
· 对一个人诚实一点
· 给自己留半小时，不刷手机

不必把今天过成「人生答案日」。
但你会在一件具体的事上，感到「对了」。`;
  }
  return `就今天来说，${n}逆位的主调是：
${k}

你今天可能会：
· 有点拖延、反复、心烦
· 对消息敏感，容易多想
· 想躲，但又内疚

今天不适合：
签大单、谈分手、做羞辱性决定

适合：
整理桌面、早睡、把最难的那 15 分钟做完`;
}

export function composeQuestion(card, rev) {
  const k = kw(card, rev);
  const n = card.name;
  if (!rev) {
    return `你心里那件放不下的事，${n}正位给出的核心是：
${k}

这不是在给你下死刑。
更像在说：
局面还有空间，但你要用「真实行动」去碰，而不是只在脑子里演。

你最近的真实状态可能是：
· 想要答案，又怕答案
· 想要人，又怕错人
· 想要改变，又怕失去熟悉

${n}建议你：
把问题缩小到「接下来三天我能做什么」
做完，你会比现在清楚一半。`;
  }
  return `你心里那件放不下的事，${n}逆位在说的是：
${k}

这不是判你失败。
是在说：
现在卡住了——时机、心态、信息，至少有一项没到位。

你最近可能：
· 反复横跳
· 等对方给信号
· 用忙掩盖不安

${n}建议你：
先停掉一种无效试探（刷存在感、冷战、反复问朋友）
先处理一种真实情绪（怕、愧、气）
外在局面，往往会跟着松一点。`;
}

export function composeAdvice(card, rev) {
  const k = kw(card, rev);
  const n = card.name;
  if (!rev) {
    return `作为接下来几周的人生指引，${n}正位希望你靠近：
${k}

具体一点：
· 做一件能积累的事（技能、存款、身体）
· 减一件你明知在拖后腿的事（习惯、承诺、关系）
· 每周留一次「不表演」的时间，对自己诚实

你不是要一夜翻盘。
你是在把自己调到「对的人、对的事更容易靠近你」的状态。`;
  }
  return `作为接下来几周的人生指引，${n}逆位在提醒：
${k}

具体一点：
· 先止血：停止一种消耗（人、工作、情绪）
· 再复盘：我写下来，我到底怕什么
· 再小步：只做一件我能完成的事

别用「等我准备好了」惩罚自己一辈子。
准备，是在行动里发生的。`;
}
