export function getManualReading(card, ctx) {
  const orientation = ctx.reversed ? 'reversed' : 'upright';
  const key =
    ctx.mode === 'single'
      ? `single.${ctx.theme}.${card.id}.${orientation}`
      : `triple.${ctx.theme}.${ctx.slot}.${card.id}.${orientation}`;
  const body = MANUAL_BODIES[key];
  return body ? { body, note: '' } : null;
}
